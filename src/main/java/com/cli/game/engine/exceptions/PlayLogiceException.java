package com.cli.game.engine.exceptions;


/**
 * 
 * @author Sankha
 *
 */
public class PlayLogiceException extends Exception {

	public PlayLogiceException() {
	}

	public PlayLogiceException(String message) {
		super(message);
	}

	public PlayLogiceException(Throwable cause) {
		super(cause);
	}

	public PlayLogiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public PlayLogiceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
